/**
 * Тесты для факторов оценки позиции
 * Каждый тест проверяет оптимальные ходы в ситуациях, где конкретный фактор должен влиять на выбор
 */

const { setupBoard, runFindBestMove, getAiConfig, test, getTests } = require('./test-framework.js');
const { assertMove, assertMoveIn, assertMoveNotIn } = require('./assertions.js');

// AI конфигурация по умолчанию для всех тестов
// Можно переопределить через CLI: --ai-config <value> или --ai-difficulty <value>
// Или изменить значение этой переменной в файле
const AI_CONFIG = getAiConfig(3);

// ========== PawnCount - Количество пешек ==========
test('PawnCount: Предпочтительнее взять пешку, чем двигаться', () => {
    // Расстановка: Чёрная пешка на c6, белая пешка на b5. Ход чёрных.
    // Кейс: Чёрные должны взять белую пешку cxb5, чтобы получить материальное преимущество, вместо движения своей пешкой вперёд.
    setupBoard('8/8/2p5/1P6/8/8/8/8 b - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'cxb5');
});

test('PawnCount: Предпочтительнее взять пешку, чем двигаться за белых', () => {
    // Расстановка: Белая пешка на c5, чёрная пешка на b6. Ход белых.
    // Кейс: Белые должны взять чёрную пешку cxb6 для материального преимущества.
    setupBoard('8/8/1p6/2P5/8/8/8/8 w - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'cxb6');
});

// ========== PotentialCaptures - Потенциальные взятия ==========
test('PotentialCaptures: Взятие лучше, чем обычный ход', () => {
    // Расстановка: Чёрная пешка на c6, белая пешка на b5. Ход чёрных.
    // Кейс: Взятие белой пешки должно быть предпочтительнее обычного хода вперёд.
    setupBoard('8/8/2p5/1P6/8/8/8/8 b - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'cxb5');
});

test('PotentialCaptures: Взятие продвинутой пешки предпочтительнее', () => {
    // Расстановка: Чёрная пешка на b6 (продвинута на 6-й ряд, ranksAdvanced=5), белая пешка на c5, белая пешка на d4, другие пешки на доске. Ход белых.
    // Кейс: Взятие более продвинутой чёрной пешки cxb6 должно быть предпочтительнее, так как она ближе к превращению и имеет большую ценность (captureBaseValue + ranksAdvanced * captureRankMultiplier = 100 + 5*20 = 200).
    setupBoard('8/pppp1ppp/1p6/2P5/3P4/8/8/8 w - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'cxb6');
});


// ========== PromotionRace - Гонка проходных ==========

test('PromotionRace: В гонке выбирать более быструю пешку', () => {
    // Расстановка: Белая пешка на e6 (distance=1, свободный путь), белая пешка на d4 (distance=3). Ход белых.
    // Кейс: В гонке проходных пешек должна продвигаться та, которая быстрее достигнет превращения. e6->e7 выигрывает гонку (raceWinBonus).
    setupBoard('8/8/4P3/8/3P4/8/8/8 w - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'e7');
});

test('PromotionRace: Гонка двух проходных пешек', () => {
    // Расстановка: Белая пешка на e7 (очень близко к превращению), белая пешка на d4. Ход белых.
    // Кейс: Пешка на e7 должна превращаться e7->e8=Q, так как она выигрывает гонку и даёт огромное преимущество.
    setupBoard('8/4P3/8/8/3P4/8/8/8 w - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'e8=Q');
});

// ========== ThreatenedPawns - Атакуемые пешки ==========
test('ThreatenedPawns: Продвинутая пешка под угрозой', () => {
    // Расстановка: Белая продвинутая пешка на e5 (ranksAdvanced>=3) атакуется чёрной на c6. Другие пешки на доске. Ход белых.
    // Кейс: Продвинутая пешка под угрозой получает дополнительный штраф (threatenedAdvancedPenalty=60), поэтому не должна двигаться e5->e6 под удар.
    setupBoard('8/pppp1ppp/2p5/4P3/8/3P4/8/8 w - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'd4');
});

// ========== MediumFreePath - Свободный путь ==========
test('MediumFreePath: Продвигать пешку со свободным путем', () => {
    // Расстановка: Белая пешка на e4, перед ней нет фигур на вертикали e до края доски. Ход белых.
    // Кейс: Пешка имеет свободный путь к превращению (isPathBlocked=false), поэтому её продвижение e4->e5 даёт бонус за свободный путь (freePathCount++).
    setupBoard('8/8/8/8/4P3/8/8/8 w - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'e5');
});

test('MediumFreePath: Свободный путь к превращению', () => {
    // Расстановка: Белая пешка на e7, перед ней свободный путь к превращению. Ход белых.
    // Кейс: Пешка имеет полностью свободный путь, поэтому превращение e7->e8=Q критично важно.
    setupBoard('8/4P3/8/8/8/8/8/8 w - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'e8=Q');
});

// ========== MediumAdjacentThreat - Боковая угроза ==========

test('MediumAdjacentThreat: Избегать опасного соседства', () => {
    // Расстановка: Чёрная пешка на d5 рядом с белой на e4. Ход белых.
    // Кейс: Избегать соседства с противником. Взять dxe5 или двигаться e4->e5, чтобы уйти от соседства.
    setupBoard('8/8/8/3p4/4P3/8/8/8 w - - 0 1');
    assertMoveIn(runFindBestMove(AI_CONFIG), ['dxe5', 'e5']);
});

test('MediumPawnAdvancement: Продвигать самую далекую пешку', () => {
    // Расстановка: Белые пешки стоят на a6, b5, c4, d3, e2 (пять диагональных пешек), ход белых.
    // Кейс: Самая продвинутая пешка на a6 должна двигаться вперед a6->a7, так как продвижение самой дальней пешки выгоднее других (medium/advanced pawn advancement).
    setupBoard('8/P7/1P6/2P5/3P4/4P3/8/8 w - - 0 1');
    assertMove(runFindBestMove(AI_CONFIG), 'a8=Q');
});

// ========== OpeningTempo - Темп в дебюте ==========
test('OpeningTempo: Делать двойной ход в дебюте', () => {
    // Расстановка: Начальная позиция - все пешки на начальных позициях (ряд 6 для белых, ряд 1 для чёрных). Ход белых (дебют, moveNumber <= 5).
    // Кейс: В дебюте двойные ходы пешками (пешки на expectedDoubleRank=4 для белых) дают бонус за темп (doubleMoveBonus), поэтому предпочтительны.
    setupBoard('8/pppppppp/8/8/8/8/PPPPPPPP/8 w - - 0 1');
    assertMoveIn(runFindBestMove(AI_CONFIG), ['a4', 'b4', 'c4', 'd4', 'e4', 'f4', 'g4', 'h4']);
});

test('OpeningTempo: Двойной ход центральной пешкой', () => {
    // Расстановка: Начальная позиция. Ход белых (дебют).
    // Кейс: Двойной ход центральными пешками (d4, e4, col в диапазоне 2-5) даёт больший бонус (centerDoubleMoveBonus=8 vs doubleMoveBonus=5), поэтому предпочтительнее.
    setupBoard('8/pppppppp/8/8/8/8/PPPPPPPP/8 w - - 0 1');
    assertMoveIn(runFindBestMove(AI_CONFIG), ['d4', 'e4']);
});

// ========== Комплексные ситуации ==========
test('Комплекс: Выбор между взятием и продвижением', () => {
    // Расстановка: Чёрная пешка на c6, белые пешки на b5, d5, e4. Ход белых.
    // Кейс: Множество вариантов: взять bxc6 (материальное преимущество), продвигать d5->d6 или e4->e5 (продвижение). Должен выбрать оптимальный баланс между факторами.
    setupBoard('8/8/2p5/1P1P4/4P3/8/8/8 w - - 0 1');
    assertMoveIn(runFindBestMove(AI_CONFIG), ['bxc6', 'dxc6', 'd6', 'b6']);
});


module.exports = getTests();
